#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned num;

	num = (unsigned)((n < 0) ? -n : n);
	if (n < 0)
		ft_putchar('-');
	if (num >= 10)
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
	else
		ft_putchar(num + '0');
}
