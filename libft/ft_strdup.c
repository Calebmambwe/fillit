#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*str2;
	int		len;
	int		i;

	len = ft_strlen(str);
	str2 = (char *)malloc(sizeof(char) * len + 1);
	if (!str2)
		return (NULL);
	i = 0;
	while (str[i])
	{
		str2[i] = str[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
